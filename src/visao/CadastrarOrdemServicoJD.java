    public void setOrdemServico(OrdemServico ordemServico) {
        this.ordemServico = ordemServico;
        spCodigoServico.setValue(ordemServico.getCodigoServico());
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        txtDataAbertura.setText(formato.format(ordemServico.getDataAbertura().getTime()));
        txtNomeCliente.setText(ordemServico.getNome());
        txtCodigoCliente.setText(String.valueOf(ordemServico.getCodigo()));
        cmbTipoVeiculo.setSelectedItem(ordemServico.getTipo());
        txtPlaca.setText(ordemServico.getPlaca());
        txtModeloVeiculo.setText(ordemServico.getModelo());
        txtAnoFabricacao.setText(ordemServico.getAnoFabricacao());
        cmbCor.setSelectedItem(ordemServico.getCor());
        cmbSituacao.setSelectedItem(ordemServico.getSituacao());
        txtDescricao.setText(ordemServico.getDescricao());
        txtValor.setText(String.valueOf(ordemServico.getValor()));

    }

