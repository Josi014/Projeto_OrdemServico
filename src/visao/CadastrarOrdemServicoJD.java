    private void bntSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntSalvarActionPerformed
        if (txtDataAbertura != null && !txtDataAbertura.getText().trim().isEmpty()) {
            try {
                SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
                Date data = formato.parse(txtDataAbertura.getText().trim());
                Calendar calendario = Calendar.getInstance();
                calendario.setTime(data);

                ordemServico.setDataAbertura(calendario);

            } catch (ParseException e) {
                e.printStackTrace();
            }
        }
        if (!txtNomeCliente.getText().trim().isEmpty()) {
            cliente.setNome(txtNomeCliente.getText().trim());
            ordemServico.setNome(txtNomeCliente.getText().trim());
        }
        if (!txtCodigoCliente.getText().trim().isEmpty()) {
            cliente.setCodigo(Integer.parseInt(txtCodigoCliente.getText().trim()));
            ordemServico.setCodigo(Integer.parseInt(txtCodigoCliente.getText().trim()));
        }
        if (spCodigoServico.getValue() != null) {
            ordemServico.setCodigoServico((Integer) spCodigoServico.getValue());
        }

        if (!txtDescricao.getText().trim().isEmpty()) {
            ordemServico.setDescricao(txtDescricao.getText().trim());
        }

        if (!txtPlaca.getText().trim().isEmpty()) {
            ordemServico.setPlaca(txtPlaca.getText().trim());
        }
        if (!txtAnoFabricacao.getText().trim().isEmpty()) {
            ordemServico.setAnoFabricacao(txtAnoFabricacao.getText().trim());
        }
        if (!txtValor.getText().trim().isEmpty()) {
            ordemServico.setValor(Integer.parseInt(txtValor.getText().trim()));
        }
        if (!txtModeloVeiculo.getText().trim().isEmpty()) {
            ordemServico.setModelo(txtModeloVeiculo.getText().trim());
        }
        if (cmbTipoVeiculo.getSelectedItem() != null) {
            ordemServico.setTipo((Tipo) cmbTipoVeiculo.getSelectedItem());
        }
        if (cmbCor.getSelectedItem() != null) {
            ordemServico.setCor((Cor) cmbCor.getSelectedItem());
        }
        if (cmbSituacao.getSelectedItem() != null) {
            ordemServico.setSituacao((Situacao) cmbSituacao.getSelectedItem());
        }

        ordemServico.setCliente(cliente);
        dispose();
    }//GEN-LAST:event_bntSalvarActionPerformed

